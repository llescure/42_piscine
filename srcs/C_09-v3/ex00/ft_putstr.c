#include <unistd.h>

void	ft_display(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_display(str[i]);
		i++;
	}
}
