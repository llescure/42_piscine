int		ft_sqrt(int nb)
{
	long i;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 0;
	while (i * i < nb)
		i++;
	if (nb == i * i)
		return (i);
	else
		return (0);
}
